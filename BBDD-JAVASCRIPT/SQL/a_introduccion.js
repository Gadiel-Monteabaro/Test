// Structured Query Language
// Si necesitamos almacenar datos como pedidos, detalles de clientes, inventario de productos. Cosas que se relacionan entre si, es mejor utilizar una base de datos SQL.
// MySql -> base de datos para SQL

// Not Only Structured Query Language
// Si tenemos un sitio web, con un solo tipo de relacion, como Por Ejemplo Instagram, donde tenemos un usuario, una contraseÃ±a, etc. Donde tambien podriamos tener una matriz de publicaciones. Es conveniente utilizar una base de datos NoSQL.
// MongoDB -> base de datos para NoSQL

// En cuanto escabilidad, NoSQL permite un sistema distribuido, lo cual nuestra base de datos puede ser distribuida entre equipos diferentes.

// SQL se ejecuta en una estructura de Tablas, NoSQL se ejecuta en una estructura de documento (JSON).

// SQL necesita de una estructura en mente antes de crear la base de datos, NoSQL es mucho mas flexible a cambios.

// SQL almacena informacion con mucha relacion entre los datos, lo cual hay mayor velocidad al momento de consultar datos.

