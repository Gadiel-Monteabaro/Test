// API -> Interfaz de de programacion de aplicaciones.
// la palabra aplicación se refiere a cualquier software con una función distinta.
// La interfaz puede considerarse como un contrato de servicio entre dos aplicaciones.
// La arquitectura de las API suele explicarse en términos de cliente y servidor. La aplicación que envía la solicitud se llama cliente, y la que envía la respuesta se llama servidor. En el ejemplo del tiempo, la base de datos meteorológicos del instituto es el servidor y la aplicación móvil es el cliente.

// ¿Que son las Api REST?

// REST significa transferencia de estado representacional. REST define un conjunto de funciones como GET, PUT, DELETE, etc.que los clientes pueden utilizar para acceder a los datos del servidor. Los clientes y los servidores intercambian datos mediante HTTP.

// La principal característica de la API de REST es que no tiene estado. La ausencia de estado significa que los servidores no guardan los datos del cliente entre las solicitudes.

//¿Que es una API web?

// Una API web o API de servicios web es una interfaz de procesamiento de aplicaciones entre un servidor web y un navegador web. Todos los servicios web son API, pero no todas las API son servicios web. La API de REST es un tipo especial de API web que utiliza el estilo arquitectónico estándar.

// ¿Que son las integraciones API?

// Las integraciones de las API son componentes de software que actualizan automáticamente los datos entre los clientes y los servidores.