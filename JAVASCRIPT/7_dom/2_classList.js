//La propiedad "classList" devuelve los nombres de clase CSS de un elemento.
const example1 = element.classList;
example1.add("myStyle"); // dentro del atributo class de nuestro elemento, agregamos la clase "myStyle"

const example2 = element.classList;
example2.remove("myStyle");// dentro del atributo class de nuestro elemento, eliminamos la clase "myStyle"

const example3 = element.classList;
example3.toggle("myStyle");// dentro del atributo class de nuestro elemento, ejecutamos la clase "myStyle", cuando sea llamado.

